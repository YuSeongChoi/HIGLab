import CoreML

/// 온디바이스 ML의 3대 장점
enum OnDeviceMLBenefits {
    /// 1. 프라이버시
    /// 사용자 데이터가 기기를 떠나지 않습니다
    /// 얼굴, 건강 데이터 등 민감한 정보도 안전하게 처리
    case privacy
    
    /// 2. 속도
    /// 네트워크 왕복 시간 없이 밀리초 단위 응답
    /// 실시간 카메라 분석, AR 등에 필수
    case speed
    
    /// 3. 오프라인 지원
    /// 인터넷 연결 없이도 동작
    /// 비행기 모드, 지하철 등에서도 사용 가능
    case offline
}

