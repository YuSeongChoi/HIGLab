import CloudKit

// 데이터베이스 비교

// ┌─────────────────────────────────────────────────────────────────┐
// │               Private Database                                  │
// │  - 사용자 개인 데이터                                            │
// │  - 커스텀 Zone 지원                                              │
// │  - 사용자 iCloud 용량 사용                                       │
// │  - 공유 원본 데이터 저장 (CKShare와 함께)                         │
// └─────────────────────────────────────────────────────────────────┘

// ┌─────────────────────────────────────────────────────────────────┐
// │               Public Database                                   │
// │  - 모든 사용자 접근 가능                                         │
// │  - 기본 Zone만 사용                                              │
// │  - 앱 개발자 CloudKit 용량 사용                                  │
// │  - 공개 데이터, 앱 설정 등                                       │
// └─────────────────────────────────────────────────────────────────┘

// ┌─────────────────────────────────────────────────────────────────┐
// │               Shared Database                                   │
// │  - 다른 사용자가 공유해준 데이터                                  │
// │  - 공유자별로 Zone이 생성됨                                      │
// │  - 원본은 공유자의 Private DB에 존재                             │
// │  - 권한에 따라 읽기/쓰기 가능                                    │
// └─────────────────────────────────────────────────────────────────┘
