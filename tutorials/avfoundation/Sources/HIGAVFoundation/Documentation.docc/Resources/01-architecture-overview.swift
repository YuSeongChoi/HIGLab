// AVFoundation 레이어 구조
//
// ┌─────────────────────────────────────┐
// │             AVKit                   │  ← 고수준 UI 컴포넌트
// │   (AVPlayerViewController 등)       │
// ├─────────────────────────────────────┤
// │          AVFoundation               │  ← 미디어 처리 API
// │  (캡처, 재생, 편집, 내보내기)         │
// ├─────────────────────────────────────┤
// │     Core Audio / Core Video         │  ← 저수준 미디어 처리
// │         Core Media                  │
// └─────────────────────────────────────┘
//
// AVFoundation 주요 기능:
// - 미디어 캡처: 카메라, 마이크 입력
// - 미디어 재생: 오디오, 비디오 재생
// - 미디어 편집: 자르기, 합치기, 필터
// - 미디어 분석: 바코드, 얼굴 인식
